peso_payaso = 112
peso_muñeca = 75
cantidad_payasos = int(input("Ingrese el número de payasos vendidos: "))
cantidad_muñecas = int(input("Ingrese el número de muñecas vendidas: "))
peso_total = (peso_payaso * cantidad_payasos) + (peso_muñeca * cantidad_muñecas)
print(f"El peso total del paquete es {peso_total} gramos.")
